function onmouseover(x){
    x.style.height = "110px";
    x.style.width = "110px";
    x.style.transition = ".75s";
}
function onmouseout(x){
    x.style.height = "100px";
    x.style.width = "100px";
    x.style.transition = ".75s";
}
function onmouseover2(x){
    x.style.fontSize = "1.4em";
    x.style.transition = ".75s";
}
function onmouseout2(x){
    x.style.fontSize = "1.2em"
    x.style.transition = ".75s";
}